n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))

s = n1 + n2
sub = n1 - n2
m = n1 * n2
d = n1 / n2
di = n1 // n2
e = n1 ** n2

print('A soma é {}, a subtração é {} e o produto é {}' .format(s, sub, m))
print('A divisão é {}, a divisão inteira é {} e a potência é {}' .format(d, di, e))